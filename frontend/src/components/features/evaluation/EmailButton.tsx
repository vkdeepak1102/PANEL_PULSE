import React from 'react';
import { Mail } from 'lucide-react';
import { useEvaluationStore } from '@/lib/stores/evaluation.store';
import { DIMENSIONS } from '@/types/evaluation.types';

const BACKEND_LABEL_TO_CAMEL: Record<string, string> = {
  'Mandatory Skill Coverage': 'mandatorySkillCoverage',
  'Technical Depth': 'technicalDepth',
  'Scenario / Risk Evaluation': 'scenarioRiskEvaluation',
  'Framework Knowledge': 'frameworkKnowledge',
  'Hands-on Validation': 'handsOnValidation',
  'Leadership Evaluation': 'leadershipEvaluation',
  'Behavioral Assessment': 'behavioralAssessment',
  'Rejection Validation Alignment': 'rejectionValidationAlignment',
};

interface Props {
  jobId: string;
  evaluationId?: string;
  panelName?: string;
  candidateName?: string;
  score?: number;
  categories?: Record<string, number> | null;
  evaluationData?: any | null;
}

export function EmailButton({
  jobId,
  evaluationId,
  panelName,
  candidateName,
  score,
  categories,
  evaluationData,
}: Props) {
  const store = useEvaluationStore();
  const effectiveScore = score ?? store.panelScore ?? 0;
  const effectiveCategories = (categories ?? store.dimensions) as Record<string, number> | null;

  function getScore(camelKey: string): number {
    if (!effectiveCategories) return 0;
    if (camelKey in effectiveCategories) return Number(effectiveCategories[camelKey] ?? 0);
    for (const [label, ck] of Object.entries(BACKEND_LABEL_TO_CAMEL)) {
      if (ck === camelKey && label in effectiveCategories) return Number((effectiveCategories as any)[label] ?? 0);
    }
    return 0;
  }

  function handleEmailClick() {
    const numScore = Number(effectiveScore);
    const scoreCategory = numScore >= 8 ? 'Good' : numScore >= 5 ? 'Moderate' : 'Poor';
    
    // Build Subject
    const subjectTitle = candidateName 
      ? `Panel Evaluation Report - ${candidateName}` 
      : `Panel Evaluation Report - ${jobId}`;
      
    const subject = encodeURIComponent(subjectTitle);

    // Build Body
    let bodyText = `Panel Evaluation Report\n`;
    bodyText += `=======================\n\n`;
    bodyText += `Job ID: ${jobId || 'N/A'}\n`;
    if (panelName) bodyText += `Panel Name: ${panelName}\n`;
    if (candidateName) bodyText += `Candidate: ${candidateName}\n`;
    if (evaluationId) bodyText += `Evaluation ID: ${evaluationId}\n`;
    bodyText += `\n`;
    bodyText += `Total Score: ${numScore.toFixed(1)} / 10.0 (${scoreCategory})\n\n`;
    
    // Dimensions
    bodyText += `Dimension Breakdown:\n`;
    bodyText += `--------------------\n`;
    Object.entries(DIMENSIONS).forEach(([camelKey, def]) => {
      const s = getScore(camelKey);
      bodyText += `- ${def.name}: ${s.toFixed(2)} / ${def.maxScore.toFixed(2)}\n`;
    });
    bodyText += `\n`;

    // Summary
    if (evaluationData?.panelSummary) {
      bodyText += `Panel Summary:\n`;
      bodyText += `--------------\n`;
      // Convert Markdown bullets to plain text bullets if they exist
      const summaryClean = evaluationData.panelSummary.replace(/\n\s*[-*]\s*/g, '\n- ');
      bodyText += `${summaryClean}\n\n`;
    }

    // L2 Reasons
    const l2Reasons: string[] =
      (evaluationData?.l2RejectionReasons?.length ?? 0) > 0
        ? evaluationData.l2RejectionReasons
        : store.l2RejectionReason ? [store.l2RejectionReason] : [];
        
    if (l2Reasons.length > 0) {
      bodyText += `L2 Rejection Reasons:\n`;
      bodyText += `---------------------\n`;
      l2Reasons.forEach(r => {
        bodyText += `- ${r}\n`;
      });
      bodyText += `\n`;
    }

    bodyText += `Generated by Panel Pulse AI`;

    const body = encodeURIComponent(bodyText);

    // Open default mail client (Outlook)
    window.location.href = `mailto:?subject=${subject}&body=${body}`;
  }

  return (
    <button
      type="button"
      onClick={handleEmailClick}
      className="inline-flex items-center gap-2 px-4 py-2 bg-indigo-500/15 border border-indigo-500/30 text-indigo-300 rounded-lg hover:bg-indigo-500/25 transition-colors text-sm font-medium"
      title="Send Report via Email"
    >
      <Mail className="w-4 h-4" />
      Email Report
    </button>
  );
}
